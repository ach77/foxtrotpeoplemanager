	public void updateform(int id_mao, int id_dedo) {

		String nomemao = "";
		cbox_mao.setSelectedIndex(id_mao - 1);
		list_dedos.setSelectedIndex(id_dedo - 1);
		switch (id_mao - 1) {
		case 0:
			nomemao = "esq";
			break;
		case 1:
			nomemao = "dir";
			break;
		default:
			nomemao = "";
			break;
		}
		// tornar padrão depois...
		lb_ver_dedo.setIcon(new javax.swing.ImageIcon(Principal.class
				.getResource("images/" + nomemao + "-"
						+ String.valueOf(id_dedo) + ".png")));

	}

